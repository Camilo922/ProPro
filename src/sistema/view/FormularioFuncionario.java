    public void listar(){
        FuncionarioDAO dao = new FuncionarioDAO();
        List<Funcionario> lista = dao.listarFuncionarios();
        DefaultTableModel dados = (DefaultTableModel) tabela.getModel();
        dados.setRowCount(0);
        
        for (Funcionario f : lista) {
            dados.addRow(new Object[]{
                 
                f.getNome(),
                f.getBi(),
                f.getCodigo(),
                f.getCargo(),
                 f.getHorario(),
                f.getUtilizador()
            });
        }
    }

