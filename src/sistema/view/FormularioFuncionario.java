    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        Medico m = new Medico();
        Funcionario f = new Funcionario();
        MedicoDAO mDAO = new MedicoDAO();
        FuncionarioDAO fDAO = new FuncionarioDAO();

        f.setNome(txtNome.getText());
        f.setBi(txtBi.getText());
        f.setHorario(txtHorario.getText());
        f.setCargo(cbxCargo1.getSelectedItem().toString());
        f.setUtilizador(txtUsu.getText());
        fDAO.salvarFuncionario(f);
        JOptionPane.showMessageDialog(null, "Funcionario Atualizado com sucesso");
        if (cbxCargo1.getSelectedItem() == "Medico") {
            m.setNumero(Integer.parseInt(txtNmedico.getText()));
            m.setEspecialidade(m.getEspecialidade());
            m.setFuncionario(f);
            mDAO.atualizarMedico(m);
            JOptionPane.showMessageDialog(null, "Medico Cadastrado com sucesso");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

