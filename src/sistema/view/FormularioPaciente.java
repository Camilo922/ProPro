    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        painelGuias.setSelectedIndex(0);
        txtNome.setText(tabela.getValueAt(tabela.getSelectedRow(), 0).toString());
        txtBi.setText(tabela.getValueAt(tabela.getSelectedRow(), 1).toString());
        String dataString = tabela.getValueAt(tabela.getSelectedRow(), 2).toString();
        txtBairro.setText(tabela.getValueAt(tabela.getSelectedRow(), 6).toString());
        txtTeste.setText(dataString);
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd"); // Ajustando o formato da data
        Date data = null;
        try {
            data = dateFormat.parse(dataString);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        dataChooser.setDate(data);
        txtTelefone.setText(tabela.getValueAt(tabela.getSelectedRow(), 3).toString());
        cbxProvincia.setSelectedItem(tabela.getValueAt(tabela.getSelectedRow(), 4).toString());
        cbxMunicipio.setSelectedItem(tabela.getValueAt(tabela.getSelectedRow(), 5).toString());
        
    }//GEN-LAST:event_tabelaMouseClicked

