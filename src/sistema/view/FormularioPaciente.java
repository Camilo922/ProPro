    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String nome = "%" + txtFiltro.getText() + "%";
        PacienteDAO dao = new PacienteDAO();
        List<Paciente> lista = dao.filtrarPacientes(nome);
        DefaultTableModel dados = (DefaultTableModel) tabela.getModel();
        dados.setRowCount(0);

        for (Paciente p : lista) {
            dados.addRow(new Object[]{
                p.getNome(),
                p.getNumeroDoc(),
                p.getDataNascimento(),
                p.getTelefone(),
                p.getProvincia(),
                p.getMunicipio(),
                p.getBairro()
            });
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        Paciente p = new Paciente();
        p.setProvincia(cbxProvincia.getSelectedItem().toString());
        p.setMunicipio(cbxMunicipio.getSelectedItem().toString());
        p.setBairro(txtBairro.getText());
        p.setNumeroDoc(txtBi.getText());
        p.setNome(txtNome.getText());
        p.setTelefone(txtTelefone.getText());
        // FORMATAR A DATA
        Date dataSelecionada = dataChooser.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dataFormatada = dateFormat.format(dataSelecionada);
        p.setDataNascimento(dataFormatada);

        PacienteDAO pDao = new PacienteDAO();
        pDao.salvarPaciente(p);

        Utilitarios util = new Utilitarios();
        util.limpaTela(jPainelDados);
        cbxProvincia.setSelectedIndex(0);
        cbxMunicipio.setSelectedIndex(0);
        dataChooser.setDate(null);
    }//GEN-LAST:event_btnSalvarActionPerformed

